      .h4 2.7. Organizaciones sindicales

    .row.justify-content-center.align-items-center.mb-5   
      .col-lg-6.mb-3 
        p.mb-3 La Constitución de 1991, en su artículo 39, establece el derecho de asociación sindical, de acuerdo con el cual se garantiza a los trabajadores y a los empleadores el derecho de constituir organizaciones sindicales sin intervención del Estado, reflejando los principios y garantías  establecidos en los Convenios 87 y 98 de la OIT. La Carta Fundamental ha determinado, de igual manera, que los sindicatos pueden establecer libremente las reglas de su estructura interna y funcionamiento; y se les ha reconocido la personería jurídica de manera inmediata.

        p.mb-3  Al desarrollar este precepto constitucional, la Corte, ha establecido que la función que cumplen las organizaciones sindicales, de defender los intereses de la clase obrera, constituye una de los fundamentos del Estado Social de Derecho, pues los sindicatos se constituyen en el único intermediario idóneo entre los trabajadores individualmente considerados y los empleadores.

